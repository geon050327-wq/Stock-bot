import streamlit as st
import time

# --- 1. 페이지 기본 설정 ---
st.set_page_config(page_title="청년 혜택 번역기", page_icon="💡", layout="centered")

# --- 2. 메인 타이틀 화면 ---
st.title("💡 인스타 DM 구걸 그만! 청년 혜택 번역기")
st.markdown("""
**"댓글 달면 DM으로 알려드려요" ➡️ 이제 그만 당하세요!** 🤬
어렵고 복잡한 정부 지원금, 이름만 입력하면 AI가 **자격 요건**과 **공식 신청 링크**를 3초 만에 번역해 드립니다.
""")

# --- 3. 검색창과 버튼 ---
query = st.text_input("🔎 궁금한 지원금 이름을 입력하세요 (예: 청년도약계좌, 내일준비적금)", placeholder="여기에 입력하세요...")

if st.button("진짜 혜택 확인하기 🚀"):
    if query:
        # 로딩 애니메이션 (진짜 일하는 척)
        with st.spinner(f"'{query}'에 대한 관공서 문서를 씹어먹는 중... 냠냠 🤖"):
            time.sleep(2) # 2초 대기 (나중에 진짜 AI 코드로 교체할 부분)

            # --- 4. 사이다 결과 출력 화면 ---
            st.success("✨ 분석 완료! 복잡한 조건 다 빼고 핵심만 알려드립니다.")
            
            st.subheader(f"🎯 [{query}] 핵심 요약")

            # 3가지 핵심 박스 (info, warning, success 색상 활용)
            st.info("**💰 그래서 나 얼마 받을 수 있는데?**\n\n(예시) 최대 월 40만 원씩 정부가 매칭해서 입금해 줌. 전역할 때 목돈 1,400만 원 수령 가능!")
            
            st.warning("**✅ 나도 해당되나? (핵심 조건 3줄)**\n\n1. 현역병, 상근예비역, 의무경찰 등 복무 중인 자\n2. 남은 복무기간이 6개월 이상일 것\n3. 은행별 월 20만 원 한도 내 가입 가능")
            
            st.success("**🔗 공식 다이렉트 신청 링크**\n\n[👉 나라사랑포털 공식 안내 바로가기](https://www.narasarang.or.kr) (인스타 낚시 아님!)")

            # --- 5. 수익화(광고) 배너 영역 (모의) ---
            st.markdown("---")
            st.markdown("💸 **[스폰서 광고]** KB국민은행에서 가입하면 치킨 기프티콘 100% 증정! [자세히 보기](#)")
    else:
        st.warning("앗! 지원금 이름을 먼저 입력해주세요. 😅")
